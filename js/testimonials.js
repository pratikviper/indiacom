// Filter testimonials by category
function filterTestimonials(category) {
  // Update active button with smooth animation
  document.querySelectorAll(".filter-btn").forEach((btn) => {
    btn.classList.remove("active", "bg-blue-900", "text-white");
    btn.classList.add("bg-gray-200", "text-gray-700");
    btn.style.transform = "scale(1)";
  });

  event.target.classList.add("active", "bg-blue-900", "text-white");
  event.target.classList.remove("bg-gray-200", "text-gray-700");
  event.target.style.transform = "scale(1.05)";

  // Show loading state with animation
  showLoadingState();

  // Simulate loading delay for better UX
  setTimeout(() => {
    displayTestimonials(category);
  }, 400);
}

// Display testimonials based on category
function displayTestimonials(category) {
  const grid = document.getElementById("testimonials-grid");
  const loadingState = document.getElementById("loading-state");
  const noResults = document.getElementById("no-results");

  // Filter testimonials
  const filteredTestimonials =
    category === "all" ? testimonialsData : testimonialsData.filter((testimonial) => testimonial.myCategory === category);

  // Hide loading state
  loadingState.classList.add("hidden");

  // Check if no results
  if (filteredTestimonials.length === 0) {
    grid.innerHTML = "";
    noResults.classList.remove("hidden");
    return;
  }

  // Hide no results state
  noResults.classList.add("hidden");

  // Generate testimonial cards with enhanced animations
  grid.innerHTML = filteredTestimonials
    .map(
      (testimonial, index) => `
    <div class="testimonial-card bg-white rounded-xl shadow-lg hover:shadow-2xl p-6 border border-gray-100 fade-in-up transform transition-all duration-500 hover:-translate-y-2" style="animation-delay: ${index * 0.1}s">
      <!-- Header with logo and basic info -->
      <div class="flex items-start space-x-4 mb-4">
        <div class="w-16 h-16 rounded-lg bg-gradient-to-br from-blue-500 to-blue-700 flex items-center justify-center text-white font-bold text-lg">
          ${testimonial.name.charAt(0)}
        </div>
        <div class="flex-1">
          <h3 class="text-xl font-bold text-blue-900 mb-1 hover:text-blue-700 transition-colors">${testimonial.name}</h3>

          <span class="inline-block px-3 py-1 bg-gradient-to-r from-blue-100 to-blue-200 text-blue-800 text-xs font-medium rounded-full capitalize transform transition-all duration-300 hover:scale-105">
            ${testimonial.myCategory.replace("_", " ")}
          </span>
        </div>
      </div>

      <!-- Action Buttons -->
      <div class="flex space-x-2 mt-4">
        ${testimonial.googleMapsPage ? `<a href="${testimonial.googleMapsPage}" target="_blank" class="flex-1 bg-blue-600 text-white px-3 py-2 rounded-lg text-sm font-medium hover:bg-blue-700 transition-colors text-center">Google Maps</a>` : ''}
        ${testimonial.google360 ? `<a href="${testimonial.google360}" target="_blank" class="flex-1 bg-green-600 text-white px-3 py-2 rounded-lg text-sm font-medium hover:bg-green-700 transition-colors text-center">360° View</a>` : ''}
      </div>
    </div>
  `,
    )
    .join("");

  // Add staggered animation to cards
  const cards = grid.querySelectorAll(".testimonial-card");
  cards.forEach((card, index) => {
    card.style.animationDelay = `${index * 0.15}s`;
  });
}

// Show loading state with enhanced animation
function showLoadingState() {
  const grid = document.getElementById("testimonials-grid");
  const loadingState = document.getElementById("loading-state");
  const noResults = document.getElementById("no-results");

  // Fade out current content
  grid.style.opacity = "0";
  grid.style.transform = "translateY(20px)";

  setTimeout(() => {
    grid.innerHTML = "";
    noResults.classList.add("hidden");
    loadingState.classList.remove("hidden");

    // Reset grid styles
    grid.style.opacity = "1";
    grid.style.transform = "translateY(0)";
  }, 200);
}

// Smooth scroll to testimonials section
function scrollToTestimonials() {
  document.getElementById("testimonials").scrollIntoView({
    behavior: "smooth",
    block: "start",
  });
}
